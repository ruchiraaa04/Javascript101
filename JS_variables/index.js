//VARIABLES IN JAVASCRIPT

//var is used to create a variable
var n =10;
console.log(n) //print the value of n assigned to it 

console.log('n') //prints n and not the value assigned to it 

//let is used to create a variable
//You cannot define another variable with same name using let, it will show error as n is already defined
//you can change the value but cannot redefine it using let

let n=10;
n=20;
console.log(n) 

//const is used to declare a constant , whose value cannot be changed
const a =10
console.log(a+10)